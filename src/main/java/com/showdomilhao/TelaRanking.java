    private void btnRemoverJogadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverJogadorActionPerformed
        // TODO add your handling code here:
                int linha = tblRanking.getSelectedRow();
        if (linha < 0) { lblStatusRanking.setText("Selecione um jogador."); return; }
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
            "Remover jogador selecionado?");
        if (confirm != javax.swing.JOptionPane.YES_OPTION) return;
        try {
            jogadorDAO.deletar(jogadoresRanking.get(linha).getId());
            carregarTudo(); // atualiza ranking e chaveamento
            lblStatusRanking.setText("Jogador removido.");
        } catch (SQLException e) {
            lblStatusRanking.setText("Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRemoverJogadorActionPerformed

    private void btnConfirmarPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarPartidaActionPerformed
        // TODO add your handling code here:
// CORRETO
Jogador j1 = cmbJ1.getSelectedItem() instanceof Jogador ? (Jogador) cmbJ1.getSelectedItem() : null;
Jogador j2 = cmbJ2.getSelectedItem() instanceof Jogador ? (Jogador) cmbJ2.getSelectedItem() : null;
        if (j1 == null || j2 == null || j1.getId() == j2.getId()) {
            lblResultadoChave.setText("⚠ Selecione dois jogadores diferentes.");
            return;
        }
        if (!rdJ1Venceu.isSelected() && !rdJ2Venceu.isSelected()) {
            lblResultadoChave.setText("⚠ Selecione o vencedor.");
            return;
        }
        Jogador vencedor = rdJ1Venceu.isSelected() ? j1 : j2;
        Jogador perdedor = rdJ1Venceu.isSelected() ? j2 : j1;
        try {
            partidaDAO.registrarPartida(j1.getId(), j2.getId(), vencedor.getId());
            jogadorDAO.adicionarPontos(vencedor.getId(), 3);  // +3 por vitória
            jogadorDAO.adicionarPontos(perdedor.getId(), 1);  // +1 por participação
            lblResultadoChave.setText("✅ " + vencedor.getNome() + " venceu! +3pts");
            carregarTudo(); // atualiza ranking e histórico em tempo real
        } catch (SQLException e) {
            lblResultadoChave.setText("Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnConfirmarPartidaActionPerformed

