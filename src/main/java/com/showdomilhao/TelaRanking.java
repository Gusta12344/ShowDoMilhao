    private void carregarRanking() {
        try {
            modeloRanking.setRowCount(0);
            jogadoresRanking = jogadorDAO.listarRanking();
            for (int i = 0; i < jogadoresRanking.size(); i++) {
                Jogador j = jogadoresRanking.get(i);
                int pos = i + 1;
                modeloRanking.addRow(new Object[]{
                    pos,
                    j.getMedalha(pos),
                    j.getNome(),
                    ControladorJogo.formatarPremio(j.getPontuacao())
                });
            }
        } catch (SQLException e) {
            lblStatusRanking.setText("Erro ao carregar ranking.");
        }
    }

private void carregarChaveamento() {
    try {
        List<Jogador> lista = jogadorDAO.listarTodos();
        // Cast para JComboBox sem tipo para aceitar objetos Jogador
        javax.swing.JComboBox combJ1 = cmbJ1;
        javax.swing.JComboBox combJ2 = cmbJ2;
        combJ1.removeAllItems();
        combJ2.removeAllItems();
        for (Jogador j : lista) {
            combJ1.addItem(j);
            combJ2.addItem(j);
        }
    } catch (SQLException e) {
        System.err.println("Erro ao carregar chaveamento.");
    }
}

    private void carregarHistorico() {
        try {
            modeloHistJogos.setRowCount(0);
            for (Object[] linha : partidaDAO.listarHistoricoJogos())
                modeloHistJogos.addRow(linha);

            modeloHistChave.setRowCount(0);
            for (Object[] linha : partidaDAO.listarHistoricoChaveamento())
                modeloHistChave.addRow(linha);
        } catch (SQLException e) {
            System.err.println("Erro ao carregar histórico.");
        }
    }
