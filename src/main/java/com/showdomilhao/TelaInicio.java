public void carregarJogadores() {
    try {
        // Cast para JComboBox sem tipo para aceitar objetos Jogador
        javax.swing.JComboBox combo = cmbJogadores;
        combo.removeAllItems();
        combo.addItem("-- selecione --");
        List<Jogador> lista = jogadorDAO.listarTodos();
        for (Jogador j : lista) combo.addItem(j);
    } catch (SQLException e) {
        System.err.println("Erro ao carregar jogadores: " + e.getMessage());
    }
}

