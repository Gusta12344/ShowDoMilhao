    private void btnJogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJogarActionPerformed
        // TODO add your handling code here:
        
        String nome = txtNome.getText().trim();
        Jogador jogador = null;
        
                try {
            if (!nome.isEmpty()) {
                // Criou/encontrou pelo nome digitado
                jogador = jogadorDAO.buscarOuCriar(nome);
            } else if (cmbJogadores.getSelectedIndex() > 0) {
                // Selecionou da lista
                jogador = (Jogador) cmbJogadores.getSelectedItem();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Digite seu nome ou selecione um jogador existente.");
                return;
            }
            new TelaJogo(jogador).setVisible(true);
            this.dispose();
        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnJogarActionPerformed

